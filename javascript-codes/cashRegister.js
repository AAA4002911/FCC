function checkCashRegister(price, cash, cid) {
    var change = {};
     var change_Needed = (cash - price).toFixed(2);
    var total = parseFloat(cid.reduce((account, next) => { return account + next[1] }, 0.0)).toFixed(2);
  
    if (Number(total) === Number(change_Needed)) {
      change.status = "CLOSED";
      change.change = cid;
    } else if (Number(total) < Number(change_Needed)) {
      change.status = "INSUFFICIENT_FUNDS";
      change.change = [];
    } else {
      const COIN = {
        "PENNY": 0.01,
        "NICKEL": 0.05,
        "DIME": 0.1,
        "QUARTER": 0.25,
        "ONE": 1,
        "FIVE": 5,
        "TEN": 10,
        "TWENTY": 20,
        "ONE HUNDRED": 100
      }
      change.status = "OPEN";
      change.change = [];
      for (let i = cid.length - 1; i >= 0; i--) {
  
        let coin_Name = cid[i][0];
        let coin_Total = parseFloat(cid[i][1]);
        let coin_Value = COIN[coin_Name];
        let coin_Amount = (coin_Total / coin_Value).toFixed(2);
        let cointoReturn = 0;
  
        while (change_Needed >= coin_Value && coin_Amount > 0) {
          change_Needed -= coin_Value;
          change_Needed = Math.round(change_Needed * 100) / 100;
          coin_Amount--;
          cointoReturn++;
        }
        if (cointoReturn > 0) {
          change.change.push([coin_Name, cointoReturn * coin_Value]);
        }
      }
      if (change_Needed !== 0) {
        change.status = "INSUFFICIENT_FUNDS";
        change.change = [];
      }
    }
    return change;
  }
